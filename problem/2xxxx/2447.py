def star(x, y, n):
    if (x // n) % 3 == 1 and (y // n) % 3 == 1:
        print(' ', end='')
    else:
        if n // 3 == 0:
            print('*', end='')
        else:
            star(x, y, n // 3)

n = int(input())

for i in range(n):
    for j in range(n):
        star(i, j, n)
    print()